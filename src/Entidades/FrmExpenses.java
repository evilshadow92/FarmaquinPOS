    private void loadData(String type, double amount, String comments){
        String insertExpenses = "INSERT INTO history_expenses (id_employee, description, amount,comments) VALUES(?, ?, ?, ?)";
        
        try(PreparedStatement pstmt = (PreparedStatement) conn.prepareStatement(insertExpenses)){
            pstmt.setInt(1, userID);
            pstmt.setString(2, type);
            pstmt.setDouble(3, amount);
            pstmt.setString(4, comments);
            pstmt.executeUpdate();
            
            
        }
        catch (SQLException e){            
            JOptionPane.showMessageDialog(rootPane, "Error al cargar el producto: " + e.getMessage());
        }
    }

