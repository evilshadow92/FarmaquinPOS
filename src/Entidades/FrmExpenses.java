    private void btnAceptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptMouseClicked
        String type = (String) cmbType.getSelectedItem();
        String comment = txtComment.getText().toUpperCase().trim();
        
        if (!type.equals("Default") && !txtAmount.getText().equals("")) {
            try{
                
                double amount = Double.parseDouble(txtAmount.getText());
                loadData(type, amount, comment);
                
                cmbType.requestFocus();
                cmbType.setSelectedItem("Default");
                txtAmount.setText("");
                txtComment.setText("");
                
                JOptionPane.showMessageDialog(this, "Datos registrados correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "La cantidad debe ser un numero.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar llenos y seleccionar el tipo de gasto", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_btnAceptMouseClicked

